# functions with parameters

# example 1
def greeting (name) :
    print("Hello" , name)
# call the function
greeting("Mark")

# example 2
def addition (num1 , num2) :
    answer = num1 + num2
    print("The sum is" , answer)
# call the function
addition(60 , 40)

# NB: A function is a block of reusable code that performs a specific function

# Qst 1
