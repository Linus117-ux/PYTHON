# loop through a list                                
languages = [ "English" , "French" , "German" , "Spanish" , "Chinese" , "Swahili"]
for langauge in languages :
    print(langauge)

countries = ["Kenya" , "Tanzania" , "Uganda" , "Lesotho" , "Togo"]
for country in countries :
    print(country)

movies = ["Spiderman" , "Superman" , "Batman" , "Hulk" , "Ironman"]
for movie in movies :
    print(movie)

artists = ["Mark" , "Amani" , "Max" , "Martha" , "Don Toliver" , "Central Cee"]
for artist in artists :
    print(artist)