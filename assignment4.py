# for loop from 2000 to 2024
for count in range (2000 , 2024 , 1) :
    print(count)

# Loop,loop through colors 
colors = ["Blue" , "Green" , "Red" , "Pink" , "Black"]
for color in colors :
    print(color)


# While loop 
counter = 20
while counter >= 1 :
    print(counter)
    # increment counter 
    counter = counter -1
